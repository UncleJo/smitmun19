                        <br>
                        <center><u><h2>SECRETARIAT</h2></u></center>
                        <br> 
                        <span id=1a></span>
                            <p>The entire staff of a Model United Nations   conference forms the Secretariat.</p>
                        <ol>
                            <h3><li>Secretary General</li></h3><span id=1b></span>

                                <p>The secretary general of a model united nations
                                is the highest authority. he/she is considered the
                                main spokesperson of the organisation. the
                                secretary-general has discretionary power over all
                                matters in the conference.</p>
                            
                            <h3><li>Deputy Secretary General</li></h3><span id=1c></span>
                                <p>He/she assists the secretary-general and looks after all the substantive issues and is fully responsible for the administrative tasks executed by the rest of the staff.</p>
                            
                            <h3><li>Director Research</li></h3>

                                <p>He/she heads the research team of the
                                organisation and has discretionary powers over
                                the rules of procedure, proceedings and judging
                                of the councils of SMITMUN.</p>
                            
                       </ol>  
                    </div>
                        
                    <div id="2">
                        <br>
                        <br>
                        <center><u><h2>Dias</h2></u></center>
                        <br><span id=2a></span>
                        <p>The group of people in charge of a Model UN committee.It generally consists of a Chair, a
                        Director, and a Rapporteur. The dais is also the raised
                        platform on which the chair traditionally sits.</p>
                        <ol>
                            <h3><li>Chair/Moderator</li></h3><span id=2b></span>
                                <p>The secretary general of a model united nations is the highest authority. he/she is considered the main spokesperson of the organisation. the secretary-general has discretionary power over all
                                matters in the conference.</p>
                            
                            <h3><li>Director(Committee)</li></h3><span id=2c></span>
                                <p> A member of the dais that oversees the creation of working papers and draft resolutions, acts as an expert on the topic, makes sure delegates accurately reflect the policy of their countries, and ensures that decorum is maintained during caucuses.</p>
                            
                            <h3><li>Rapporteur</li></h3>
                                <p>A member of the dais whose duties include
                                keeping the speakers' list and taking the roll call,as well as assisting in and keeping track ofadministrative duties in the committee room.</p>
                            
                        </ol>
                    </div>
                        
                    <div id="3">
                        <br>
                        <br>
                        <center><u><h2>DELEGATE</h2></u></center>
                        <br>
                        <p>A Model United Nations delegate is a participant
                        who represents the position of a country other than
                        his country of origin, and deliberates on issues from the country's perspective, and NOT the delegate's perspective or point of view. The delegates will play the positions of national representatives such as Ambassadors, Ministers, and other diplomats or representatives of intergovernmental Organizations (IGOs) and Non-Governmental Organizations (NGOs). It will be wrong for a MUN Delegate to argue from his/her perspective which contradicts that of the country he/she is representing.</p>
                    </div>
                    
                    <div id="4">
                        <br>
                        <br>
                        <center><u><h2>USHER/LOGISTICS MEMBER</h2></u></center>
                        <br>
                        <p>A Model United Nations Usher plays a very
                            important role during the conference. The Usher runs all the errands during the session and takes care of documents meant for distribution. Since delegates are not allowed to communicate directly with each other during the session, all communications are channelled through the Usher.</p>
                    </div>
                        
                    <div id="5">
                        <br>
                        <br>
                        <center><u><h2>Attire</h2></u></center>
                        <br>
                        <ul>
                            <li>Professional business attire required.</li>
                            <li>National symbols of any kind are forbidden.</li>
                            <li>Only symbols associated with UN are allowed.</li>
                        </ul>
                    </div>
                        
                    <div id="6">
                        <br>
                        <br>
                        <center><u><h2>MUN Decorum</h2></u></center>
                        <br>
                        <ul>
                            <li>No vulgarities and slangs (e.g. YOLO, LOL) in council. Formality and respect for one another  forms the basis of this council.</li>
                            <li>Communicate with each other via note paper. You can only speak when the chair recognizes you.</li>
                            <li>Speak in third person e.g. “This delegate” or “Country ABC”. Address your counterparts as “The delegate of ABC”.</li>
                            <li>NEVER address yourself “the government of ABC”
                            as you are not representing the government of a
                            country.</li>
                        </ul>
                    </div>
                        
                    <div id="7">
                        <br>
                        <br>
                        <center><u><h2>Session Flow</h2></u></center>
                        <br>
                        <ol>
                            <li>Roll Call</li>
                            <li>General Speakers List</li>
                            <li>General Debate (No particular focus)</li>
                            <li>Moderated Caucus (Specific aspect of the issue)</li>
                            <li>Un-moderated Caucus</li>
                            <li>Introduction of Resolution</li>
                            <li>Debate on Resolution</li>
                            <li>Debate on Amendments</li>
                            <li>Voting</li>
                        </ol>
                    </div>
                        
                    <div id="8">
                        <br>
                        <br>
                        <center><u><h2>Roll Call</h2></u></center>
                        <br>
                        <p>When the RAPPORTEUR/CHAIR calls your country, <b>raise your placard</b> and reply <b>“Present and Voting”</b> OR <b>“Present”</b>.</p>
                        <ul>
                        <li><h3>Present and Votingter</h3></li>
                        <p>When delegates announce themselves as being “present and voting”, they give away their right to abstain (i.e. not vote) on the substantive matters.</p>
                            
                        <li><h3>Present</h3></li>
                        <p>When delegates announce themselves as being PRESENT, it is simply an acknowledgement that
                        they are ready for debate, but do not necessarily have a strong opinion on it. A delegate responding “Present” has three options while voting on a substantive issue.</p>
                        <ol>
                            <li>In favour</li>
                            <li>Against</li>
                            <li>Abstain</li>
                        </ol>
                        </ul>
                    </div>
                        
                    <div id="9">
                        <br>
                        <br>
                        <center><u><h2>Substantive and Procedural Matters</h2></u></center>
                        <br>
                        <ul>
                            <li><h3>SUBSTANTIVE MATTERS</h3></li>
                            <p>This includes votes on introducing draft resolutions and working paper’s, as well as the final vote. Delegates who said <b>“PRESENT”</b> during roll call can abstain.</p>
                            <li><h3>SUBSTANTIVE MATTERS</h3></li>
                            <p>This includes votes on motions for moderated and unmoderated caucus, and appeals. In a <b>PROCEDURAL</b> VOTE, no delegate may abstain.</p>
                        </ul>
                    </div>
                        
                    <div id="10">
                        <br>
                        <br>
                        <center><u><h2>Formal Debate</h2></u></center>
                        <br>
                        <p> The standard type of debate at model UN conference, in which delegates speak for a certain time in an order based on a speaker’s list.</p>
                    </div>
                        
                    <div id="11">
                        <br>
                        <br>
                        <center><u><h2>Speakers List</h2></u></center>
                        <br>
                        <ul>
                            <li>A list that determines the order in which the delegate’s will speak.</li>
                            <li>A speakers list provides a delegate the opportunity to express his/her country’s stand on the agenda.</li>
                            <li>A speakers list remains open throughout the discussion of the topic area unless committee chooses to move into a moderated caucus or un-moderated caucus.</li>
                            <li>The time limit is pre-set to one and a half minute per delegate.</li>
                            
                        </ul>
                    </div>
                    
                    <div id="12">
                        <br>
                        <br>
                        <center><u><h2>Motion to Set Speaker's Time Limit</h2></u></center>
                        <br>
                        <ul>
                            <li>This is a motion to set or change the speaker's
                            time limit.</li>
                            <li>This motion can be introduced in the
                            following way<b><br><br><center><q>“Honourable Chair, the delegation from INDIA moves to set a limit on the speaker’s time of three minute.”</q></center></b></li>
                        </ul>
                    </div>
                    
                    <div id="13">
                        <br>
                        <br>
                        <center><u><h2>Speech Rules and Procedures</h2></u></center>
                        <br>
                        <p>Each speaker is alloted limited time to speak (including time for speaking and answering POIs.<br>In case there is some time left after the speaker is finished speaking. The speaker can yeald the floor to either <b>a speaker</b> or <b>the chair</b>.
                        <br><br>At the end of your speech, state if you are open to questions or not.</p>
                    </div>
                        
                    <div id="14">
                        <br>
                        <br>
                        <center><u><h2>Opening Speech</h2></u></center>
                        <br>
                        <p>An opening speech is a mean for other countries in the council to know your country’s position on the issue.</p>
                        <p>In your opening speech should reflect on</p>
                        <ul>
                            <li>Brief history on the issue.</li>
                            <li>How the issue affects your country?</li>
                            <li>Your country’s position on the issue.</li>
                            <li>Discuss past UN, member states of NGO action. </li>
                            <li>Your country’s expectations from the resolution.</li>
                        </ul>
                    </div>
                    
                    <div id="15">
                        <br>
                        <br>
                        <center><u><h2>Point of Information</h2></u></center>
                        <br>
                        <ul>
                            <li>After a delegate has given a speech in formal
                            debate,he or she may yield time to points of 
                            information,question from other delegates
                            concerning the speech. </li>
                            <li>One may raise his/her placard and on being
                            recognized may ask for valid documentation
                            supporting the delegate’s facts.</li>
                            <li><b>This point CANNOT interrupt a speaker.</b>
                        </ul>
                    </div>
                    
                    <div id="16">
                        <br>
                        <br>
                        <center><u><h2>Point of Personal Privilege</h2></u></center>
                        <br>
                        <ul>
                            <li>This point can be raised by any delegate at
                            anytime, when the request concerns the wellbeing of the delegate.</li>
                            <li>Only the Point of Personal Privilege relating to inaudibility can interrupt a speech.</li>
                            <li>Other examples include being excused to use the washroom.</li>
                            <li><b>This point CAN interrupt a speaker.</b>
                        </ul>
                    </div>
                    
                    <div id="17">
                        <br>
                        <br>
                        <center><u><h2>Point of Parliamentry Inquiry</h2></u></center>
                        <br>
                        <ul>
                            <li>This point can be raised by any delegate at any time, addressed to the Chair, when the delegate wishes to enquire about a parliamentary procedure.</li>
                            <li><b>This point CANNOT interrupt a speaker.</b>
                        </ul>
                    </div>
                        
                    <div id="18">
                        <br>
                        <br>
                        <center><u><h2>Point of Order</h2></u></center>
                        <br>
                        <ul>
                            <li>A delegate may raise a Point of Order
                            whenever delegates believes the chair has
                            made an error in the running of the committee.</li>
                            <li>The chair will then immediately rule on the
                            Point of Order.</li>
                            <li><b>This point CAN interrupt a speaker.</b>
                        </ul>
                    </div>
                    
                    <div id="19">
                        <br>
                        <br>
                        <center><u><h2>Right to Reply</h2></u></center>
                        <br>
                        <ul>
                            <li>It is used by the delegate when he feels that his opinions have been violated.</li>
                            <li><b>This point CANNOT interrupt a speaker.</b>
                        </ul>
                    </div>
                        
                    <div id="20">
                        <br>
                        <br>
                        <center><u><h2>Challenge</h2></u></center>
                        <br>
                        <ul>
                            <li>This is when a delegate can challenge another delegate over a particular issue for a specified period of time, this may be over a sub-topic or country policy.</li>
                            <li>This is a one on one debate.</li>
                        </ul>
                    </div>
                        
                    <div id="21">
                        <br>
                        <br>
                        <center><u><h2>Caucus</h2></u></center>
                        <br>
                        <p>It's a  break in formal debate in which countries can more easily and informally discuss a topic. </p>
                        <span id="21a"></span>
                        <p>There are two types of Caucuses</p>
                        <ol>
                            <li><h3>MODERATED CAUCUS</h3></li>
                            <ul>
                            <li>A detailed discussion on a sub topic of the main agenda is possible in a moderated caucus and the main reason of
                            starting a moderated caucus is to know the committee’s opinion on such an issue.</li>
                            <li>A delegate may raise a motion for a moderated caucus in the following manner:</li>
                                <br><center><q>“Motion to open a moderated caucus to discuss <b>[topic]</b> for the time duration <b>[specified by the delegate max 20 mins]</b> and time per speaker<b> [Defined by the delegate max 1 minute]</b>.”</q></center><br><span id="21b"></span>
                            <li>A motion for a moderated caucus requires a simple majority to pass and can be passed through an informal vote.</li>
                            <li>A moderated caucus can be started in the middle of the speakers list, i.e. It is not compulsory for the delegates to finish hearing all the speakers on the list.</li>
                            
                            </ul>
                           
                            <li><h3>UNMODERATED CAUCUS</h3></li>
                            <ul>
                            <li>Allows delegates to informally talk to other delegates outside of debate.</li>
                            <li>A delegate may raise a motion for a moderated caucus in the following manner:</li>
                                <br><center><q>“Motion to open a unmoderated caucus to discuss <b>[topic]</b> for the time duration <b>[specified by the delegate max 20 mins]</b>."</q></center><br>
                            <li>Delegates are allowed to move around freely. Usually, it is used to allow for delegates to introduce resolutions working paper and gain the support of delegates.</li>
                            <li>When the time expires, the committee will move back into general debate.</li>
                            </ul>
                        </ol>
                    </div>
                    

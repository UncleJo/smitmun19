                        <center><u><h2>Types of Majority</h2></u></center>
                        <br>
                        <ul>
                            <li><b>Simple Majority: </b>Requires the support of 51% the of countries present.</li>
                            <li><b>Clear Majority: </b> Requires the support of 2/3<sup>rd</sup> of the countries present.</li>
                        </ul>
                            

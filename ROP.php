                        <br>
                        <br>
                        <center><u><h2>Friendly and Unfriendly Ammendments</h2></u></center>
                        <br>
                        <ul>
                            <li>Approved draft resolutions are modified through amendments. An amendment is a written statement that adds, deletes or revises an operative clause in a draft resolution.</li>
                            <li>The amendment process is used to strengthen consensus on a resolution by allowing delegates to change certain sections. There are two types of amendments.</li><br>
                            <center><h3>1.FRIENDLY AMENDMENTS</h3></center>
                            <ul>
                                <li> A friendly amendment is a change to the draft resolution that all the authors agree with.</li>
                                <li>After the amendment is signed by all of the draft resolution's author’s and approved by the committee director or president, it will be automatically incorporated into the resolution.</li>
                            </ul>
                            <center><h3>2.UNFRIENDLY AMENDMENTS</h3></center>
                            <ul>
                                <li>An unfriendly amendment is a change that some or all of the draft resolution's author’s do not support and must be voted upon by the committee. </li>
                                <li> The author(s) of the amendment will need to obtain a required number of signatories in order to introduce it (usually 20 percent of the committee). Prior to voting on the draft resolution, the committee votes on all unfriendly amendments.</li>
                            </ul>
                        </ul>
                    </div>
                    
                    <div id="30">
                        <br>
                        <br>
                        <center><u><h2>Bringing a resolution to the floor</h2></u></center>
                        <br>
                        <ul>
                            <li>A draft resolution must always gain the support of a certain number of member states in the committee before the authors (the delegates who created the resolution) may submit it to the committee staff</li>
                            <li>Authors have to raise a motion to introduce the resolution.</li>
                            <li><b>A minimum of 1 author and 20% of the voting members as signatories are required for a resolution to be introduced in the council.</b></li>
                        </ul>
                    </div>
                        
                    <div id="31">
                        <br>
                        <br>
                        <center><u><h2>Formal Voting</h2></u></center>
                        <br>
                        <ul>
                            <li>In voting procedure, there is no talking, note passing, or moving around by the delegates. Doors to the committee room should be sealed, and no  one allowed in or out (unless it is an emergency).</li>
                            <li>Voting can be in the form of placard voting, when the chair asks who is <b>for</b>, who is <b>against</b>, and who <b>astains</b>, and the delegates raise their placards for each.</li>
                        </ul>
                            
                    </div>
                        

                   </div>
                        
                   <div id="23">
                        <br>
                        <br>
                        <center><u><h2>Working Paper</h2></u></center>
                        <br>
                        <ul>
                            <li>These are the committee’s views on a particular subtopic of the main agenda in hand. They indicate the direction and inclination of the committee.</li>
                            <li>National symbols of any kind are forbidden.</li>
                            <li>Before a formal introduction to the committee, a working paper needs to be the approved by the chair.</li>
                            <li>TIn case of multiple working papers, the chair has the discretion in putting which one to vote first.</li>
                            <li>Working papers, which are drafted by delegates and voted upon by the committee, require a clear majority to pass.</li>
                            <li>The voting on a working paper can be done in an informal way by showing the placard.</li>
                            <li>If a working paper is passed it has to be incorporated in the resolution.</li>
                        </ul>
                    </div>
                        
                    <div id="24">
                        <br>
                        <br>
                        <center><u><h2>Resolution</h2></u></center>
                        <br>
                        <ul>
                            <li>The solution to the entire agenda is called a resolution.</li>
                            <li>Resolutions, which are drafted by delegates and voted upon by the committee, normally require a clear majority to pass.</li>
                            <li>There can be <b>at most 2 authors</b> to a resolution.</li>
                            
                        </ul>
                    </div>
                    
                    <div id="25">
                        <br>
                        <br>
                        <center><u><h2>Draft Resolutions</h2></u></center>
                        <br>
                        <ul>
                            <li>Draft resolutions are all resolutions that have not yet been voted on.</li> 
                            <li>Delegates write draft resolutions alone or with other countries. There are three main parts to a draft resolution: the eading, the preamble and the operative section.</li>
                            <li><b>Signatories</b>are countries that may or may not agreewith the substance of the draft resolution but still wish to see it debated so that they can propose amendments</li>
                            <li>The heading shows the committee and topic along with the resolution number. It also lists the draft resolution's sponsors and signatories (<a href= "#26">see below</a>).</li>
                            <li>Each draft resolution is one long sentence with sections separated by commas and semicolons.</li>
                            
                        </ul>
                    </div>                
                        
                    <div id="26">
                        <br>
                        <br>
                        <center><u><h2>Authors and Signatories</h2></u></center>
                        <br>
                        <ul>
                            <li><b>Authors</b> of a draft resolution are the principal writers ofthe document. Although it is possible to have only one author. Authors control a draft resolution and only the author can approve immediate changes</li>
                            <li><b>Signatories</b>are countries that may or may not agreewith the substance of the draft resolution but still wish to see it debated so that they can propose amendments</li>
                            <li>A certain percentage of the committee must be either sponsors or signatories to a draft resolution in order for it to be accepted.</li>
                            
                        </ul>
                    </div>
                        
                    <div id="27">
                        <br>
                        <br>
                        <center><u><h2>Sponsors</h2></u></center>
                        <br>
                        <ul>
                            <li>Sponsor of a resolution are the people who contribute to the resolution but are not the principal writers.</li>
                            <li>Sponsors cannot vote against the resolution.</li>
                            <li>Sponsors are approved by the authors of a resolution.</li>
                            <li><b>The number of sponsors for a resolution paper cannot exceed 10%(floor value) of the total number of voting countries present.</b></li>
                            
                        </ul>
                    </div>
                        
                    <div id="28">
                        <br>
                        <br>
                        <center><u><h2>Pre-ambulatory Clauses</h2></u></center>
                        <br>
                        <ul>
                            <li>These are the clauses which are an introduction to the resolution or solution and often refer to past resolutions, speeches and the UN charter.</li>
                            <li>Every pre-ambulatory clause starts with any one of the following pre-ambulatory phrases and ends with a comma.</li>
                            
                        </ul>

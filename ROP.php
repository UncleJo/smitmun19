                        <center><u><h2>Veto</h2></u></center>
                        <br>
                        <ul>
                            <li>The power of veto refers to the power that the 5 permanent members of the united nations security council have which enables them to prevent to adoption of any draft resolution presented by the nation.</li>
                            <li>The veto is used whenever the negative vote is cast by the permanent member of the united nations and utomatically causes resolution to fail.</li>
                            <li>The five permanent members are: </li>
                            <ol>
                                <li>China</li>
                                <li>France</li>
                                <li>Russia</li>
                                <li>United kingdom</li>
                                <li>United States of America</li>
                            </ol>
                        </ul>
                            

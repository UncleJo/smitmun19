                        <div class="cd-faq-content">
                            <p>Model United Nations is a simulation of the UN General Assembly and other multilateral bodies. In an MUN students typically roleplay delegates to the United Nations. While playing their roles as ambassadors, student "delegates" make speeches, prepare draft resolutions, negotiate with allies and adversaries, resolve conflicts, and navigate the Model UN conference rules of procedure - all in the interest of mobilizing international cooperation to resolve problems that affect countries all over the world. The delegates' in-depth knowledge of their countries guarantees a lively and memorable experience. </p>
